package little.ant.cms.service;

import org.apache.log4j.Logger;

import little.ant.platform.service.BaseService;
import little.ant.platform.common.SplitPage;
import little.ant.platform.common.DictKeys;
import little.ant.cms.model.Download;

public class DownloadService extends BaseService {

	@SuppressWarnings("unused")
	private static Logger log = Logger.getLogger(DownloadService.class);
	
	public static final DownloadService service = new DownloadService();
	
	/**
	 * 分页
	 * @param splitPage
	 */
	public void list(SplitPage splitPage) {
		String select = " select * ";
		splitPageBase(DictKeys.db_dataSource_main, splitPage, select, "cms.download.splitPage");
	}
	
	/**
	 * 删除
	 * @param ids
	 */
	public void delete(String ids){
		String[] idsArr = splitByComma(ids);
		for (String id : idsArr) {
			Download.dao.deleteById(id);
		}
	}
	
}
